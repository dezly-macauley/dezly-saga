# NOTE: Division
# This gives you the answer with the decimal points
print(7 / 3)
# 2.3333333333333335

#______________________________________________________________________________

# NOTE: Integer Division
# This gives you the answer without any decimals. 
# There is no rounding up or down

print(7 // 3)
# 2

#______________________________________________________________________________
# NOTE: Modulo 
# This gives you the remainder after and Integer Division
# E.g. 7 // 3 = 2
# This means that 3 can fit into the number 7, two times 
# 3 x 2 = 6
# So 7 - 6 = 1
print(7 % 3)
#1

