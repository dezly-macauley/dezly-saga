// SECTION: Assignment Operators

/*
Names and Grouping

    = (Assignment Operator):
        Name: Assignment Operator
        Function: Assigns the value on the right to the variable on the left.
        Example: let x = 10;

    += (Addition Assignment Operator):
        Name: Addition Assignment Operator
        Function: Adds the right operand to the left operand and assigns the result to the left operand.
        Example: x += 5; // Equivalent to x = x + 5

    -= (Subtraction Assignment Operator):
        Name: Subtraction Assignment Operator
        Function: Subtracts the right operand from the left operand and assigns the result to the left operand.
        Example: x -= 3; // Equivalent to x = x - 3

    *= (Multiplication Assignment Operator):
        Name: Multiplication Assignment Operator
        Function: Multiplies the left operand by the right operand and assigns the result to the left operand.
        Example: x *= 2; // Equivalent to x = x * 2

    /= (Division Assignment Operator):
        Name: Division Assignment Operator
        Function: Divides the left operand by the right operand and assigns the result to the left operand.
        Example: x /= 2; // Equivalent to x = x / 2

    **= (Exponentiation Assignment Operator):
        Name: Exponentiation Assignment Operator
        Function: Raises the left operand to the power of the right operand and assigns the result to the left operand.
        Example: x **= 3; // Equivalent to x = x ** 3

    ++ (Increment Operator):
        Name: Increment Operator
        Function: Increases the value of the operand by 1. It can be used in prefix (++x) or postfix (x++) form.
        Example: x++; // Equivalent to x = x + 1

    -- (Decrement Operator):
        Name: Decrement Operator
        Function: Decreases the value of the operand by 1. It can be used in prefix (--x) or postfix (x--) form.
        Example: x--; // Equivalent to x = x - 1
*/

