my_list = [ "a", "b", "c", "d"]

# NOTE: This is a for loop

for item in my_list:
    print(item)

#------------------------------------------------------------------------------

# NOTE: This is a for loop with the range function

# This will print out 1 to 9. Remember that range excludes the last value
for item in range(1, 10):
    print(item)

#------------------------------------------------------------------------------

# NOTE: While Loop

fruits = 8

while fruits > 2:
    fruits -= 1
    print("You have ", fruits, "left")

#------------------------------------------------------------------------------
